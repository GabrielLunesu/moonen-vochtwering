const slugify = (value) => value
  .toLowerCase()
  .normalize('NFD')
  .replace(/[\u0300-\u036f]/g, '')
  .replace(/[^a-z0-9]+/g, '-')
  .replace(/(^-|-$)/g, '');

export const LIMBURG_PLACE_OPTIONS = LIMBURG_PLACES.map((place, index) => {
  const hasMunicipality = place.municipality && place.municipality !== place.name;
  const label = hasMunicipality ? `${place.name} (${place.municipality})` : place.name;

  return {
    ...place,
    key: `${slugify(place.name)}-${slugify(place.municipality || `place-${index}`)}-${index}`,
    label,
    searchLabel: hasMunicipality ? `${place.name} ${place.municipality}` : place.name,
  };
});

